package com.netease.lowcode.extension.structures;

public class PDFNode {

    /**
     * 节点类型
     */
    public String type;
    /**
     * 当前节点字体大小
     */
    public Integer fontSize;
    /**
     * 字体颜色
     */
    public String fontColor;
    /**
     * 布局:居中、左对齐、右对齐
     */
    public String textAlignment;
    /**
     * 是否加粗
     */
    public Boolean bold;
    /**
     * 文本内容
     */
    public String text;

}
